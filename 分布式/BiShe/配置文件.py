# cookies
def get_cookie_dict(temp):
    # temp表示, '头条'或者‘微博'
    """
    :param temp: 门户网站名称 ， '头条'或者‘微博
    :return: 返回cookie
    """
    files = open('cookies.csv', mode='r', encoding='utf-8', newline='')
    list_cookies = csv.DictReader(files).__next__()
    files.close()
    # return list_cookies[temp]
    return {i.split('=')[0]: i.split('=')[-1] for i in list_cookies[temp].split('; ')}


