"""
需求：有变量`a = 10`和`b = 20`，交换两个变量的值。
"""

"""方法1"""
#
# a = 10
# b = 20
#
# # 定义一个中间变量
# c = 0
#
# c = a  # 将a的值存放到c变量上
# # print(c)
#
# a = b  # 将b的值存放到a变量上
# # print(a)
#
# b = c  # 将c的值存放到b变量上
# # print(b)
#
# print('a现在的值为: ', a)
# print('b现在的值为: ', b)

"""方法2"""
a, b = 10, 20
print('a现在的值为: ', a)
print('b现在的值为: ', b)

a, b = b, a
print('a现在的值为: ', a)
print('b现在的值为: ', b)